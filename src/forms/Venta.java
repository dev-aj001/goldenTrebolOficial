    private void CargarProducto(){
        Producto mProduc;
        if(mDB.Conectar()){
            String [] Datos = new String[7];
            mProduc = mDB.GetProducto(txtCodigo.getText());
            if(mProduc != null){
                Datos[0]=String.valueOf(mProduc.getCodigo());
                Datos[1]=String.valueOf(mProduc.getDescripcion());
                Datos[2]=txtCantidad.getText();
                Datos[3]=String.valueOf(mProduc.getPrecioVen());
                Datos[4]=String.valueOf(mProduc.getDepartameto());
                Datos[5]=String.valueOf(mProduc.getExistencia());;
                tbm1.addRow(Datos);
            
                
                this.tablaProduc.setModel(tbm1);
                this.tablaProduc.getColumnModel().getColumn(0).setPreferredWidth(42);
                this.tablaProduc.getColumnModel().getColumn(1).setPreferredWidth(42);
                this.tablaProduc.getColumnModel().getColumn(2).setPreferredWidth(42);
                this.tablaProduc.getColumnModel().getColumn(3).setPreferredWidth(42);
                this.tablaProduc.getColumnModel().getColumn(4).setPreferredWidth(42);
                this.tablaProduc.getColumnModel().getColumn(5).setPreferredWidth(42);
                
                if(this.tablaProduc.getRowCount()>0){
                    this.tablaProduc.setRowSelectionInterval(0, 0);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Producto no encontrado");
            }
        }
        
    }
    
    private void CargarTicket(){
        Double exis = Double.valueOf(tbm1.getValueAt(tablaProduc.getSelectedRow(), 5).toString());
        Double cant = Double.valueOf(txtCantidad.getText());
        
        
        Producto mProduc;
        if(exis>=cant){
            
            mProduc = mDB.GetProducto(txtCodigo.getText());
            if(mProduc != null){
                String descrip = mProduc.getDescripcion();
                Double precio = (mProduc.getPrecioVen()*cant);
                
                ticket.agregarProducto(descrip, cant, precio);
                
                quitarExistencia();
                LimpiarTabla1();
                CargarProducto();
                
            }
        }else{
                JOptionPane.showMessageDialog(null, "No hay suficiente existencia de este producto");
            }
        
    }
    
    private void quitarExistencia(){
        if(mDB.Conectar()){
            mDB.reducirExistencia(txtCodigo.getText(), Integer.parseInt(txtCantidad.getText()));
        }
    }
    
    private void reponerExistencia(){
        if(mDB.Conectar()){
            mDB.aumentarExistencia(txtCodigo.getText(), Integer.parseInt(txtCantidad.getText()));
        }
    }
    
    private void LimpiarTabla1(){
        for (int i = 0; i < tablaProduc.getRowCount(); i++) {
            tbm1.removeRow(i);
            i-=1;
        }
    }
    
    private void limpiarTicket(){
        ticket.limpiar();
        lblTotal.setText("$00.00");
    }
    
